import java.util.*;
public class ArithmeticOperators
{
    public static void main(String args[])
    {
        int a=10,b=5,c;
        c=a+b;
        System.out.println("a+b="+c);
        c=a-b;
        System.out.println("a-b="+c);
        c=a*b;
        System.out.println("a*b="+c);
        c=a/b;
        System.out.println("a/b="+c);
        c=a%b;
        System.out.println("a%b="+c);
    }
}